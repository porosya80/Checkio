def checkio(maze_map):
    meta = {}
    pos = (1, 1)
    goal = (10, 10)
    queue = [pos]
    visited = set()
    meta[pos] = (None, None)
    while queue:
        point = queue.pop(0)
        if point == goal:
            return''.join(construct_path(point, meta))
        children = looking_for_neigb(point[0], point[1], maze_map)
        for child, path in children:
            if child in visited:
                continue
            if child not in queue:
                meta[child] = (point, path)
                queue.append(child)
        visited.add(point)

    return

def construct_path(state, meta):
    path = []
    while meta[state][0] is not None:
        state, action = meta[state]
        path.append(action)
    path.reverse()
    return path

def looking_for_neigb(row,col,maze_map):
    result = []
    if maze_map[row - 1][col] != 1:
        result.append(((row-1, col), "N"))
    if maze_map[row + 1][col] != 1:
        result.append(((row + 1, col), "S"))
    if maze_map[row][col - 1] != 1:
        result.append(((row, col - 1), "W"))
    if maze_map[row][col] != 1:
        result.append(((row, col + 1), "E"))
    return result




